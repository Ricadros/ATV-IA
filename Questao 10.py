#10 - Faça um Programa que leia três números e mostre-os em ordem decrescente.

numero1 = float(input('Digite o primeiro número'))
numero2 = float(input('Diggite o segundo número'))
numero3 = float(input('digite o terceiro numero'))

sequencia = [numero1,numero2,numero3]

ordem = sorted(sequencia)

print ('a ordem correta',ordem)

                
        