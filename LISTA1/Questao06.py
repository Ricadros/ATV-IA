#6 - Faça um Programa que calcule a área de um quadrado, em seguida mostre o dobro desta área para o usuário.

altura = float(input('DIGITE A ALTURA DO QUADRADO'))
base = float(input('DIGITE A BASE DO QUADRADO'))

area = altura*base 

dobro = area*2 

print('o dobro da area desse quadrado é',dobro)
